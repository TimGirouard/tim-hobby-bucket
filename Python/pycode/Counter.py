# Counter
# Demonstrates the range() function

print("Counting:")
for j in range(15):
    print(j, end=" ")

print("\n\nCounting by fives:")
for k in range(0, 100, 5):
    print(k, end=" ")

print("\n\nCounting backwards by twos:")
for l in range(20, 0, -2):
    print(l, end=" ")

input("\n\nPress the enter key to exit.\n")
